    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ArrayList<String> resultado= new ArrayList<String>();
        ArrayList<Campos> nuevaConsulta=new ArrayList<Campos>();
        if(cbxPublico.getSelectedIndex()==0){//si es publico, provincia, org.gestor
            resultado=datos.mostrarNombres(true, txtProvincia.getText(), txtLocalidad.getText(), txtOrganismoGestor.getText());    
            nuevaConsulta= new ArrayList<Campos>();
            Campos nuevo;
            for(int i=0;i<resultado.size();i++){
                nuevo=separar(true,resultado.get(i));
                nuevaConsulta.add(nuevo);
            }
        
        }
        else if(cbxPublico.getSelectedIndex()==1){//no es publico, provincia, localidad
            resultado=datos.mostrarNombres(false, txtProvincia.getText(), txtLocalidad.getText(), txtOrganismoGestor.getText());
            nuevaConsulta= new ArrayList<Campos>();
            Campos nuevo;
            for(int i=0;i<resultado.size();i++){
                nuevo=separar(false,resultado.get(i));
                nuevaConsulta.add(nuevo);
            }
        }
        
        rModel.listaNombres=nuevaConsulta;
        rModel.fireTableDataChanged();
    }//GEN-LAST:event_btnBuscarActionPerformed

    public Campos separar(boolean condicion, String frase){
        Campos campos= new Campos();
        if(condicion==true){
            StringTokenizer st = new StringTokenizer(frase, "_");
//            while (st.hasMoreElements()) {    
                campos.setTitulo(st.nextElement().toString());
                campos.setLocalidad("");
                campos.setOrganismoGestor(st.nextElement().toString());
                campos.setProvincia(st.nextElement().toString());
//            }
        }           
            
        else{
            StringTokenizer st = new StringTokenizer(frase, "_");
//            while (st.hasMoreElements()) {    
                campos.setTitulo(st.nextElement().toString());
                campos.setLocalidad(st.nextElement().toString());
                campos.setProvincia(st.nextElement().toString());
                campos.setOrganismoGestor("");
//            }
        }
        
        
        return campos;
    }
    
        public class RModel extends AbstractTableModel{
//        ArrayList<Sala> listaSalas=new ArrayList<Sala>();//Reemplazar por llamada a BD obtener todo
        public ArrayList<Campos> listaNombres = new ArrayList<Campos>();
        
        String [] titles = {"Titulo","Provincia","Localidad","Org. Gestor"};
        
        @Override
        public String getColumnName(int col){   
            return titles[col];   
        }
        
        @Override
        public int getRowCount() {
            return listaNombres.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Object getValueAt(int row, int col) {
            String value = "";
            switch(col){
		case 0:  value = listaNombres.get(row).getTitulo(); break;                
                case 1:  value = listaNombres.get(row).getProvincia(); break;
		case 2:  value = listaNombres.get(row).getLocalidad(); break;
                case 3:  value = listaNombres.get(row).getOrganismoGestor(); break;
                
            }
            return value;            
        }
        
    
    
    }

    
